#Nao precisa indicar o tipo da variavel em Python, a propria linguagem constroi a mais adequada

a = 2
print(type(a))
#Output: <class 'int'>
b = 987746463737885857637
print(type(b))
#Output: <class 'int'>
pi = 3.14
print(type(pi))
#Output: <class 'float'>
c = 'A'
print(type(c))
#Output: <class 'str'> str = string
q = True
print(type(q))
#Output: <class 'bool'>
x = None
print(type(x))
#Output: <class 'NoneType'>
